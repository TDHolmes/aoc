const EXAMPLE_INPUT: &str = "";

fn part_one(_input: &str) -> i32 {
    let mut result = 0;
    result += 2;

    result
}

fn part_two(_input: &str) -> i32 {
    let mut result = 0;
    result += 2;

    result
}

#[test]
fn example_part_one() {
    let result = part_one(EXAMPLE_INPUT);
    assert_eq!(result, 2);
}

#[test]
fn example_part_two() {
    let result = part_two(EXAMPLE_INPUT);
    assert_eq!(result, 2);
}

#[test]
fn test_part_one() {
    assert_eq!(1 + 1, 2);
}

